# *参数传递
def fun(a, b):
    print('a= ', a)
    print('b= ', b)


print('----------位置参数传递-------------')
fun(1, 2)
print('----------关键字参数传递-------------')
fun(b=2, a=1)
print('----------位置&关键字参数传递-------------')
fun(1, b=2)  # 位置在前
print('----------*列表参数传递-------------')
lst = [1, 2]
fun(*lst)  # 列表前加*，将列表的每个元素都当作一个位置参数传递

print('----------**字典参数传递-------------')
dic = {'b': 1, 'a': 2}
fun(**dic)
