# 字符串类型
""""
    1. 可以使用单引号' '、双引号" "、三引号''' '''或者""" """定义
    2. 单双引号定义的字符串必须在一行
    3. 三引号定义的字符串可以分布在连续的多行（中间的换行或者Tab原样输出）
"""
str1 = '我爱python'
print(str1)
str2 = "我爱python"
print(str2)
str3 = '''我爱编程，
    我爱python'''
print(str3)
str4 = """我爱编程，
我爱python"""
print(str4)
